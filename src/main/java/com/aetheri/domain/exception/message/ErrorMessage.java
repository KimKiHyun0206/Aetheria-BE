package com.aetheri.domain.exception.message;

import lombok.AccessLevel;
import lombok.Getter;
import lombok.RequiredArgsConstructor;
import org.springframework.http.HttpStatus;

/**
 * 애플리케이션에서 발생하는 다양한 오류 상황에 대한 **표준화된 에러 메시지**와
 * 이에 상응하는 **HTTP 상태 코드({@link HttpStatus})**를 관리하는 열거형 클래스입니다.
 *
 * <p>각 상수는 고유한 오류 유형을 나타내며, 비즈니스 로직 및 외부 통신 오류를 포괄합니다.</p>
 */
@Getter
@RequiredArgsConstructor(access = AccessLevel.PROTECTED)
public enum ErrorMessage {
    // -----------------------------------------------------------------------------------------------------------------
    // Server 에러 (General)
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * 클라이언트로부터 전달받은 요청 파라미터가 유효하지 않을 때 발생합니다. (400 Bad Request)
     */
    INVALID_REQUEST_PARAMETER(HttpStatus.BAD_REQUEST, "잘못된 요청 입니다."),
    /**
     * 서버 측에서 예상치 못한 내부 오류가 발생했을 때 발생합니다. (500 Internal Server Error)
     */
    INTERNAL_SERVER_ERROR(HttpStatus.INTERNAL_SERVER_ERROR, "예기치 못한 에러가 발생했습니다"),
    /**
     * 인증은 되었으나, 해당 리소스에 접근할 권한이 없을 때 발생합니다. (403 Forbidden)
     */
    FORBIDDEN(HttpStatus.FORBIDDEN, "유효하지 않은 접근입니다"),

    // -----------------------------------------------------------------------------------------------------------------
    // 데이터베이스 커넥션 에러 (R2DBC)
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * R2DBC 데이터베이스 연결 자체에 실패했을 때 발생합니다. (500 Internal Server Error)
     */
    R2DBC_CONNECTION_ERROR(HttpStatus.INTERNAL_SERVER_ERROR, "데이터베이스 연결에 실패했습니다."),
    /**
     * 데이터베이스 연결 풀에서 커넥션을 가져오는 데 실패했을 때 발생합니다. (500 Internal Server Error)
     */
    R2DBC_CONNECTION_ACQUISITION_ERROR(HttpStatus.INTERNAL_SERVER_ERROR, "데이터베이스 연결을 얻을 수 없습니다."),

    // -----------------------------------------------------------------------------------------------------------------
    // 카카오 API/인증 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * 카카오 인증 응답에서 인증 코드({@code code})를 찾을 수 없을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_AUTHORIZATION_CODE(HttpStatus.NOT_FOUND, "인증 코드를 응답에서 찾을 수 없습니다"),
    /**
     * 카카오 리프레시 토큰이 유효하지 않거나 만료되었을 때 발생합니다. (404 Not Found)
     */
    INVALID_REFRESH_TOKEN(HttpStatus.NOT_FOUND, "카카오 리프레쉬 토큰이 존재하지 않습니다"),
    /**
     * 카카오 API 통신 중 액세스 토큰을 찾을 수 없을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_ACCESS_TOKEN(HttpStatus.NOT_FOUND, "카카오 액세스 토큰을 찾을 수 없습니다."),
    /**
     * 카카오 API 통신 중 리프레시 토큰을 찾을 수 없을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_REFRESH_TOKEN(HttpStatus.NOT_FOUND, "카카오 리프레쉬 토큰을 찾을 수 없습니다."),

    // -----------------------------------------------------------------------------------------------------------------
    // JWT 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * JWT의 Subject 클레임이 예상하는 숫자 형식이 아닐 때 발생합니다. (400 Bad Request)
     */
    JWT_SUBJECT_IS_NOT_NUMBER(HttpStatus.BAD_REQUEST, "JWT의 subject는 숫자여야 합니다."),
    /**
     * JWT 토큰 문자열이 {@code null}이거나 비어있을 때 발생합니다. (400 Bad Request)
     */
    JWT_TOKEN_IS_EMPTY(HttpStatus.BAD_REQUEST, "JWT가 null입니다."),
    /**
     * JWT 토큰이 만료되었을 때 발생합니다. (401 Unauthorized)
     */
    JWT_EXPIRED(HttpStatus.UNAUTHORIZED, "JWT가 만료되었습니다."),
    /**
     * JWT 토큰의 서명이 유효하지 않거나 구조에 문제가 있을 때 발생합니다. (401 Unauthorized)
     */
    JWT_INVALID(HttpStatus.UNAUTHORIZED, "JWT가 유효하지 않습니다."),


    // -----------------------------------------------------------------------------------------------------------------
    // Redis 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * Redis에서 해당 키에 대한 리프레시 토큰을 찾지 못했을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_REFRESH_TOKEN_IN_REDIS(HttpStatus.NOT_FOUND, "Redis에서 리프레쉬 토큰을 찾지 못했습니다."),


    // -----------------------------------------------------------------------------------------------------------------
    // 사용자 (Runner) 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * 요청된 사용자를 데이터베이스에서 찾지 못했을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_RUNNER(HttpStatus.NOT_FOUND, "요청한 사용자를 찾지 못했습니다."),
    /**
     * 이미 동일한 정보(예: 카카오 ID)로 가입된 사용자가 존재할 때 발생합니다. (409 Conflict)
     */
    DUPLICATE_RUNNER(HttpStatus.CONFLICT, "중복된 사용자입니다."),


    // -----------------------------------------------------------------------------------------------------------------
    // 이미지 메타데이터 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * 요청된 이미지 메타데이터(DB 레코드)를 찾지 못했을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_IMAGE_METADATA(HttpStatus.NOT_FOUND, "이미지 메타데이터를 찾지 못했습니다."),
    /**
     * 특정 이미지 메타데이터에 대한 소유자만이 수행할 수 있는 작업(수정, 삭제 등)을 소유자가 아닌 사용자가 시도할 때 발생합니다. (403 Forbidden)
     */
    RUNNER_IS_NOT_OWNER_OF_IMAGE_METADATA(HttpStatus.FORBIDDEN, "이미지의 소유자가 아닙니다."),

    // -----------------------------------------------------------------------------------------------------------------
    // 이미지 파일 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * 저장되거나 조회하려는 이미지 경로(Path)가 유효하지 않을 때 발생합니다. (404 Not Found)
     */
    INVALID_IMAGE_PATH(HttpStatus.NOT_FOUND, "유효한 이미지 경로가 아닙니다."),
    /**
     * 저장소(예: S3, 로컬 파일 시스템)에서 실제 이미지 파일을 찾지 못했을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_IMAGE(HttpStatus.NOT_FOUND, "이미지를 조회하지 못했습니다."),

    // -----------------------------------------------------------------------------------------------------------------
    // 카카오 토큰 (데이터베이스) 에러
    // -----------------------------------------------------------------------------------------------------------------
    /**
     * 데이터베이스에 저장된 카카오 토큰 정보(액세스/리프레시 토큰)를 찾지 못했을 때 발생합니다. (404 Not Found)
     */
    NOT_FOUND_KAKAO_TOKEN(HttpStatus.NOT_FOUND, "카카오 토큰을 데이터베이스에서 찾을 수 없습니다"),


    ;


    /**
     * 클라이언트에게 반환될 HTTP 응답 상태 코드입니다.
     */
    private final HttpStatus status;
    /**
     * 개발자 및 사용자에게 제공될 상세 오류 메시지입니다.
     */
    private final String message;
}